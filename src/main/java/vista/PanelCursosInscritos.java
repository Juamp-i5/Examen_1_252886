    public void agregarCurso(int id, String nombre, double precio) {
        panelContenedorCursos.add(new PanelCurso(id, nombre, precio, false, controlador));
        panelContenedorCursos.add(Box.createVerticalStrut(ESPACIO_ENTRE_CURSOS));

        this.total += precio;
        actualizarLabelCantidadTotal();
        
        botonFinalizarInscripcion.setEnabled(true);
    }

    public void quitarTodosLosCursos() {
        panelContenedorCursos.removeAll();
        panelContenedorCursos.revalidate();
        panelContenedorCursos.repaint();

        total = 0;
        actualizarLabelCantidadTotal();

        botonFinalizarInscripcion.setEnabled(false);
    }

    private void actualizarLabelCantidadTotal() {
        this.labelCantidadTotal.setText("$" + String.format("%.2f", total));
    }

